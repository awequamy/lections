# команды Git 
# 1) git init= создает новый гит репозиторий локальный на вашем пк
# 2) git commit - как только мы достигаем определенного момента в разработке то тогда мы сохраняем и комментируем изменения
# git commit -m '<comment>'
# 3) git add - когда мы создаем или изменяем файлы то при помощи этой команды мы загружаем все изменения в локальное хранилище(репозиторий)
# git add <file name>
# or
# git add .


















